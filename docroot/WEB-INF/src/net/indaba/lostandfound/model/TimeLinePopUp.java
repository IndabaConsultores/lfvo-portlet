package net.indaba.lostandfound.model;

public class TimeLinePopUp {

	String date_es;
	String date_eu;
	String name;
	String image;
	String lat;
	String lng;
	
	public String getDate_es() {
		return date_es;
	}
	
	public void setDate_es(String date_es) {
		this.date_es = date_es;
	}
	
	public String getDate_eu() {
		return date_eu;
	}
	
	public void setDate_eu(String date_eu) {
		this.date_eu = date_eu;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getImage() {
		return image;
	}
	
	public void setImage(String image) {
		this.image = image;
	}
	
	public String getLat() {
		return lat;
	}
	
	public void setLat(String lat) {
		this.lat = lat;
	}
	
	public String getLng() {
		return lng;
	}
	
	public void setLng(String lng) {
		this.lng = lng;
	}	
}